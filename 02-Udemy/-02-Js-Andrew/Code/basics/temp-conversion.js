let fahrenheit = 50; // 0c - 273.15k

// Calculate celsius and store in celsius variable
// Print that value

let celsius = (fahrenheit - 32) * (5 / 9);
console.log(celsius);

// Calculate kelvin value and store in variable
// Print the value

let kelvin = (fahrenheit + 459.67) * (5 / 9);
console.log(kelvin);
